package entites;

/**
 *
 * @author HP OMEN
 */
public class Destination {
    private int id;
    private String nom;

    public Destination() {
    }

    public Destination(int id, String nom) {
        this.id = id;
        this.nom = nom;
    }
    
    

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the nom
     */
    public String getNom() {
        return nom;
    }

    /**
     * @param nom the nom to set
     */
    public void setNom(String nom) {
        this.nom = nom;
    }
    
}
